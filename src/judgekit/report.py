"""Self-contained HTML eval report for a JudgeHarness / EvalResult.

The output is a single ``.html`` file with plotly assets inlined — no CDN
dependency. You can drop it in an email, attach it in CI, or open it
locally with no internet. Components:

- Header: summary stats (n, point estimate, CI, drift verdict).
- Reliability diagram (calibration curve): per-decile fraction-positive
  vs. mean predicted probability. Closer to the diagonal = better
  calibrated.
- Score distribution histograms: reference vs. current run, overlaid.
- Drift gauge: PSI value with the warn / fail thresholds drawn.
- Per-pair / per-item table (top + bottom N).

The HTML template is a single Jinja2 string that we render with the
plotly figure JSON inlined. Optional dependency: ``plotly`` and
``jinja2``. Install via the ``[report]`` extra::

    pip install judgekit[report]
"""

